# Learning typecasting in python


# There are two types of typecasting:
# 1- Implicit typecasting
# 2- Explicit typecasting


# Exploring Explicit typecasting(done by the programmer)
a="5"
b="8"
print(int(a)+int(b))


# Exploring Implicit typecasting(done by the python interpreter)
c=0.5
d=8
print(c+d)





